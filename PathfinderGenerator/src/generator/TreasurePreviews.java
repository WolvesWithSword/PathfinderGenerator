package generator;

public class TreasurePreviews {
    private String name;
    private int Po;
    private int nbItem;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getPo() {
        return Po;
    }

    public void setPo(int po) {
        Po = po;
    }

    public int getNbItem() {
        return nbItem;
    }

    public void setNbItem(int nbItem) {
        this.nbItem = nbItem;
    }
}
